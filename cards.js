function appendGeneratedCards(type, target, startIndex, createCard) {
  const usedIds = new Set(Object.values(GAME_CARDS).flat().map((card) => card.id));
  let nextIndex = startIndex;

  while (GAME_CARDS[type].length < target) {
    const card = createCard(nextIndex);

    if (!usedIds.has(card.id)) {
      GAME_CARDS[type].push(card);
      usedIds.add(card.id);
    }

    nextIndex += 1;
  }
}

function expandGeneratedCards() {
  appendGeneratedCards("action", FULL_CARD_TARGETS.action, 31, makeActionCard);
  appendGeneratedCards("truth", FULL_CARD_TARGETS.truth, 16, makeTruthCard);
  appendGeneratedCards("never", FULL_CARD_TARGETS.never, 11, makeNeverCard);
  appendGeneratedCards("mini_game", FULL_CARD_TARGETS.mini_game, 16, makeMiniGameCard);
  appendGeneratedCards("chaos", FULL_CARD_TARGETS.chaos, 11, makeChaosCard);
}

