function padCardNumber(number) {
  return String(number).padStart(3, "0");
}

function generatedTimer(index, values) {
  return index % 4 === 0 ? values[index % values.length] : null;
}

function makeActionCard(index) {
  const scene = ACTION_SCENES[index % ACTION_SCENES.length];
  const twist = ACTION_TWISTS[Math.floor(index / ACTION_SCENES.length) % ACTION_TWISTS.length];

  return {
    id: `action_${padCardNumber(index)}`,
    type: "action",
    title: "Действие",
    timer: generatedTimer(index, [20, 25, 30, 40]),
    text: `{player}, ${scene}. ${twist}`
  };
}

